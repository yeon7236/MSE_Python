def print_max(a, b, c) :
    max_val = 0
    if a > max_val :
        max_val = a
    if b > max_val :
        max_val = b
    if c > max_val :
        max_val = c
    print(max_val)
#if 문을 이용해 max_val 보다 클 때 a, b, c를 이용하면 max(a, b, c)함수가 도출된다.
